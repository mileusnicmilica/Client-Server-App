    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Neophodno je selektovati red!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PutovanjeTabela model = (PutovanjeTabela) jTable1.getModel();
        int red = jTable1.getSelectedRow();
        Putovanje p = model.getPutovanje(red);
        System.out.println(p.toString());

        if (p.isEditable()) {
            DodajPutovanjeForma dpf = new DodajPutovanjeForma(this, p, ulogovani);
            dpf.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Prijava se može izmeniti maksimalno 48h do datuma ulaska u zemlje EU.", "Greska", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

