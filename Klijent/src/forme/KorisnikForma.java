    void popuniTabelu() {
        List<Putovanje> putovanja;
        try {
            putovanja = Controller.getInstance().getPutovanje(ulogovani);
            PutovanjeTabela tm = new PutovanjeTabela(putovanja);
            jTable1.setModel(tm);
        } catch (IOException ex) {
            Logger.getLogger(KorisnikForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(KorisnikForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

